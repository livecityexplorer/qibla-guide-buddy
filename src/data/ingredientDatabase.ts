export function findIngredient(query: string): IngredientInfo | undefined {
  const q = query.toLowerCase().trim();
  
  // First check pork keywords — highest priority
  if (isPorkRelated(q)) return PORK_INFO;
  
  return ingredientById.get(q) 
    || ingredientByName.get(q) 
    || ingredientByENumber.get(q)
    || ingredientDatabase.find(i => 
      i.otherNames.some(n => n.toLowerCase() === q) ||
      i.name.toLowerCase().includes(q) ||
      i.otherNames.some(n => n.toLowerCase().includes(q))
    );
}

export function analyzeIngredientList(ingredients: string[]): { ingredient: string; info: IngredientInfo | null; status: HalalStatus }[] {
  return ingredients.map(name => {
    const info = findIngredient(name);
    return { ingredient: name, info: info || null, status: info?.status || "unknown" };
  });
}

export function getOverallStatus(results: { status: HalalStatus }[]): HalalStatus {
  if (results.some(r => r.status === "haram")) return "haram";
  if (results.some(r => r.status === "mushbooh")) return "mushbooh";
  if (results.some(r => r.status === "unknown")) return "mushbooh";
  return "halal";
}

